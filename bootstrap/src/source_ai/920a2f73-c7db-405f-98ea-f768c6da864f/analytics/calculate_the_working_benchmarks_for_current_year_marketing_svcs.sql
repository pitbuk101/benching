WITH
    WORKING_DATA AS (
        SELECT
            YEAR (TO_DATE (PER.DIM_DATE, 'YYYYMMDD')) AS YEAR,
            SOUR.TXT_CATEGORY_LEVEL_2,
            SOUR.TXT_CATEGORY_LEVEL_3,
            SUM(MES_SPEND_CURR_1) AS WORKING_SPEND
        FROM
            DATA.VT_C_FACT_INVOICEPOSITION_MULTIPLIED IVM
            JOIN DATA.VT_DIM_PERIOD PER ON PER.DIM_DATE = IVM.DIM_DATE
            JOIN DATA.VT_C_DIM_SOURCINGTREE_TECHCME SOUR ON SOUR.DIM_SOURCING_TREE = IVM.DIM_SOURCING_TREE
        WHERE
            LOWER(SOUR.TXT_CATEGORY_LEVEL_3) = LOWER('Working')
            AND LOWER(SOUR.TXT_CATEGORY_LEVEL_2) = LOWER('Marketing SVCs')
        GROUP BY
            YEAR (TO_DATE (PER.DIM_DATE, 'YYYYMMDD')),
            SOUR.TXT_CATEGORY_LEVEL_2,
            SOUR.TXT_CATEGORY_LEVEL_3
    ),
    MARKETING_DATA AS (
        SELECT
            YEAR (TO_DATE (PER.DIM_DATE, 'YYYYMMDD')) AS YEAR,
            SOUR.TXT_CATEGORY_LEVEL_2,
            SUM(MES_SPEND_CURR_1) AS MARKETING_SPEND
        FROM
            DATA.VT_C_FACT_INVOICEPOSITION_MULTIPLIED IVM
            JOIN DATA.VT_DIM_PERIOD PER ON PER.DIM_DATE = IVM.DIM_DATE
            JOIN DATA.VT_C_DIM_SOURCINGTREE_TECHCME SOUR ON SOUR.DIM_SOURCING_TREE = IVM.DIM_SOURCING_TREE
        WHERE
            LOWER(SOUR.TXT_CATEGORY_LEVEL_2) = LOWER('Marketing SVCs')
        GROUP BY
            YEAR (TO_DATE (PER.DIM_DATE, 'YYYYMMDD')),
            SOUR.TXT_CATEGORY_LEVEL_2
    )
SELECT
    WD.YEAR,
    WD.TXT_CATEGORY_LEVEL_2 AS CATEGORY,
    WD.TXT_CATEGORY_LEVEL_3 AS SUBCATEGORY,
    MARKETING_SPEND,
    75 AS WORKING_BENCHMARK,
    WORKING_SPEND,
    ROUND((WORKING_SPEND / MARKETING_SPEND) * 100, 0) AS ACTUAL_WORKING_SPEND,
    (WORKING_BENCHMARK - ACTUAL_WORKING_SPEND) / 100 * MARKETING_SPEND AS ACTUAL_WORKING_SPEND_GAP
FROM
     WORKING_DATA WD
    JOIN MARKETING_DATA MD ON MD.YEAR = WD.YEAR
    AND MD.TXT_CATEGORY_LEVEL_2 = WD.TXT_CATEGORY_LEVEL_2