WITH SUPPLIER_SPEND AS (
    SELECT 
        YEAR(TO_DATE(PER.DIM_DATE, 'YYYYMMDD')) AS YEAR,
        SOUR.TXT_CATEGORY_LEVEL_2 AS CATEGORY,
        SUP.DIM_SUPPLIER AS SUPPLIER_ID, 
        SUP.TXT_CONS_SUPPLIER_L1 AS SUPPLIER_NAME,
        SUM(IVP.MES_SPEND_CURR_1) AS SPEND
    FROM DATA.VT_C_FACT_INVOICEPOSITION_MULTIPLIED IVP
    JOIN DATA.VT_DIM_PERIOD PER ON PER.DIM_DATE = IVP.DIM_DATE
    JOIN DATA.VT_C_DIM_SOURCINGTREE_TECHCME SOUR ON SOUR.DIM_SOURCING_TREE = IVP.DIM_SOURCING_TREE
    JOIN DATA.VT_C_DIM_SUPPLIER SUP ON SUP.DIM_SUPPLIER = IVP.DIM_SUPPLIER
    WHERE 
        LOWER(SOUR.TXT_CATEGORY_LEVEL_2) = LOWER('Bearings')
        AND YEAR(TO_DATE(PER.DIM_DATE, 'YYYYMMDD')) = YEAR(CURRENT_DATE)
    GROUP BY 
        YEAR(TO_DATE(PER.DIM_DATE, 'YYYYMMDD')),
        SOUR.TXT_CATEGORY_LEVEL_2,
        SUP.DIM_SUPPLIER,
        SUP.TXT_CONS_SUPPLIER_L1
),
TOTAL_SPEND AS (
    SELECT 
        YEAR,
        CATEGORY,
        SUM(SPEND) AS TOTAL_SPEND
    FROM SUPPLIER_SPEND
    GROUP BY
        YEAR,
        CATEGORY
),
SUPPLIER_RANKED AS (
    SELECT 
        SUP.YEAR,
        SUP.CATEGORY,
        SUP.SUPPLIER_ID,
        SUP.SUPPLIER_NAME,
        SUP.SPEND,
        TOTAL.TOTAL_SPEND,
        SUM(SUP.SPEND) OVER (PARTITION BY SUP.YEAR, SUP.CATEGORY ORDER BY SUP.SPEND DESC) AS CUMULATIVE_SPEND,
        SUM(SUP.SPEND) OVER (PARTITION BY SUP.YEAR, SUP.CATEGORY ORDER BY SUP.SPEND DESC) / TOTAL.TOTAL_SPEND * 100 AS CUMULATIVE_PERCENTAGE
    FROM SUPPLIER_SPEND SUP
    JOIN TOTAL_SPEND TOTAL ON SUP.YEAR = TOTAL.YEAR AND SUP.CATEGORY = TOTAL.CATEGORY
)
SELECT 
  DISTINCT
    YEAR,
    CATEGORY,
    SPEND AS TOTAL_SPEND,
    SUPPLIER_NAME AS TAIL_SUPPLIERS
FROM SUPPLIER_RANKED
WHERE CUMULATIVE_PERCENTAGE > 80