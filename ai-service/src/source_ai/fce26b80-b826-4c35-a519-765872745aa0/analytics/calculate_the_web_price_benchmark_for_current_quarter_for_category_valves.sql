SELECT
    YEAR,
    QUARTER,
    CATEGORY,
    COUNTRY,
    PLANT,
    SUPPLIER,
    MATERIAL,
    PRODUCT_LINK,
    WEBSITE,
    SUM(SPEND) AS SPEND,
    SUM(QUANTITY) AS QUANTITY,
    SUM(ACTUAL_UNIT_PRICE) AS ACTUAL_UNIT_PRICE,
    SUM(BENCHMARK_UNIT_PRICE) AS BENCHMARK_UNIT_PRICE,
    SUM(SAVING_OPPORTUNITY) AS SAVING_OPPORTUNITY,
    AVG(SAVING_OPPORTUNITY_PERCENTAGE) AS SAVING_OPPORTUNITY_PERCENTAGE
FROM
    DATA.T_C_WEB_PRICE_BENCHMARK
WHERE
    YEAR = YEAR(CURRENT_DATE)
    AND QUARTER = QUARTER(CURRENT_DATE)
    AND LOWER(CATEGORY) = LOWER('Valves')
GROUP BY 
    YEAR,
    QUARTER,
    CATEGORY,
    COUNTRY,
    PLANT,
    SUPPLIER,
    MATERIAL,
    PRODUCT_LINK,
    WEBSITE
ORDER BY SAVING_OPPORTUNITY DESC